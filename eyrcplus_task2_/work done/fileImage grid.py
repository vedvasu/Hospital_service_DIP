############################################
## Import OpenCV
import numpy as np
import cv2
############################################

############################################
## Read the image
img = cv2.imread('1.jpg')
############################################

############################################
## Do the processing
############################################
n=5
w, h, c = img.shape
v=h/(n+1)
for z in range(1,n+1):
    cv2.line(img,(z*v,0),(z*v,w),(0,0,255),1)
v=w/(n+1)
for z in range(1,n+1):
    cv2.line(img,(0,z*v),(h,z*v),(0,0,255),1)
############################################
## Show the image
cv2.imshow('image',img)
############################################

############################################
## Close and exit
cv2.waitKey(0)
cv2.destroyAllWindows()
############################################
